# Ввод расходов за каждый день
mon = int(input("Введите расход за понедельник: "))
tue = int(input("Введите расход за вторник: "))
wed = int(input("Введите расход за среду: "))
thu = int(input("Введите расход за четверг: "))
fri = int(input("Введите расход за пятницу: "))
sat = int(input("Введите расход за субботу: "))
sun = int(input("Введите расход за воскресенье: "))

# Общая сумма
total = mon + tue + wed + thu + fri + sat + sun

# Средний расход
average = total // 7

print("Общая сумма расходов:", total)
print("Средний расход в день:", average)